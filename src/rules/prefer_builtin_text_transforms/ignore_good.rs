use super::rule;
use crate::LintContext;


#[test]
fn test_good_str_replace() {
    let good = "'hello world' | str replace 'world' 'universe'";
    LintContext::test_with_parsed_source(good, |context| {
        let violations = (rule().check)(&context);
        assert_eq!(violations.len(), 0);
    });
}

